function toggleLanguage() {
  const currentLang = localStorage.getItem("lang") || "en";
  const newLang = currentLang === "en" ? "ar" : "en";
  localStorage.setItem("lang", newLang);

  applyTranslations(newLang);
  renderTestimonials(newLang);
  renderPartners(newLang);

  // ✅ تغيير نص زر اللغة
  document.querySelectorAll(".langLabel").forEach(el => {
    el.textContent = newLang === "ar" ? "EN" : "AR";
  });

  // 🧠 أعد تهيئة كاروسيل الشركاء
  $('.partners-carousel').trigger('destroy.owl.carousel');
  $('.partners-carousel').html($('.partners-carousel').html());
}



function applyTranslations(lang) {
  // ✅ ترجمة النصوص داخل العناصر
  document.querySelectorAll("[data-translate]").forEach(el => {
    const key = el.getAttribute("data-translate");
    if (key === "typewriter") return; // استثناء خاص بـ typewriter
    if (translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  // ✅ تعيين placeholder للعناصر المدعومة
  document.querySelectorAll("[data-translate-placeholder]").forEach(el => {
    const key = el.getAttribute("data-translate-placeholder");
    if (translations[lang][key]) {
      el.setAttribute("placeholder", translations[lang][key]);
    }
  });

  // ✅ تعيين value للعناصر مثل buttons أو input[type=submit]
  document.querySelectorAll("[data-translate-value]").forEach(el => {
    const key = el.getAttribute("data-translate-value");
    if (translations[lang][key]) {
      el.setAttribute("value", translations[lang][key]);
    }
  });

  // ✅ الاتجاه العام للصفحة (يمين/يسار)
  document.body.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");

  // ✅ تغيير زر التبديل بين اللغتين
  document.querySelectorAll(".langLabel").forEach(el => {
    el.textContent = lang === "ar" ? "EN" : "AR";
  });

  // ✅ تشغيل الكتابة المتحركة
  startTypewriter(lang);
}



function startTypewriter(lang) {
  const phrases = translations[lang].typewriter;
  const el = document.getElementById("typewriter");

  let i = 0;      // الجملة الحالية
  let j = 0;      // الحرف الحالي داخل الجملة
  let isDeleting = false;

  function type() {
    if (!el) return;

    const currentPhrase = phrases[i];

    // كتابة الجملة أو مسحها
    el.innerText = currentPhrase.slice(0, j) + (isDeleting ? "" : "|");

    if (isDeleting) {
      j--;
      if (j < 0) {
        isDeleting = false;
        i = (i + 1) % phrases.length;
        setTimeout(type, 1000); // استنى قبل ما يكتب الجملة الجديدة
        return;
      }
    } else {
      j++;
      if (j > currentPhrase.length) {
        setTimeout(() => {
          isDeleting = true;
          type();
        }, 2000); // استنى بعد ما يكتب الجملة كاملة
        return;
      }
    }

    setTimeout(type, isDeleting ? 150 : 200); // سرعة الحذف والكتابة
  }

  type();
}



